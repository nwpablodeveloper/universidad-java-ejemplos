package v10.reglas.para.definir.una.variable.en.java;

public class V10ReglasParaDefinirUnaVariableEnJava {
    public static void main(String[] args) {
        
        // caracteres permitidos al principio de la variable
        // _ $ 
        
        var miVariable = 1;
        var _miVariable = 2;
        var $miVaraible = 3;
        
        // NO SE RECOMIENDA letras con acentos
        // ñ Ñ á é í ó ú
        
        // No permitido
        // !"#%&/()
        
        
    }
}
