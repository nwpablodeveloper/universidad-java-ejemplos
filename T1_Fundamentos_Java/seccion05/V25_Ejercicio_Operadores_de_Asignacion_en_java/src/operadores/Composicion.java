package operadores;

public class Composicion {

    public static void main(String[] args) {

        int a = 0;
        
        a += 5;
        System.out.println("a = " + a);
        
        a += 4;
        System.out.println("a = " + a);
        
        a -= 2;
        System.out.println("a = " + a);
        
        a *= 5;
        System.out.println("a = " + a);
        
        a /= 7;
        System.out.println("a = " + a);
        
        a %= 2;
        System.out.println("a = " + a);
        
    }
}
