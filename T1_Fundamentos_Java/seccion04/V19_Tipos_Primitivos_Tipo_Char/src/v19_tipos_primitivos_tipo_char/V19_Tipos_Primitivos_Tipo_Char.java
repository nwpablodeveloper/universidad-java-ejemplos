package v19_tipos_primitivos_tipo_char;

public class V19_Tipos_Primitivos_Tipo_Char {

    public static void main(String[] args) {

        char miCaracter = 'a';
        System.out.println("miCaracter = " + miCaracter);
        char miCaracter2 = '\u0061';
        System.out.println("miCaracter2 = " + miCaracter2);
        char miCaracter3 = 97;
        System.out.println("miCaracter3 = " + miCaracter3);
    
        var miCaracter4 = 'a';
        System.out.println("miCaracter4 = " + miCaracter4);
        var miCaracter5 = '\u0061';
        System.out.println("miCaracter5 = " + miCaracter5);
        var miCaracter6 = 97;
        System.out.println("miCaracter6 = " + miCaracter6);
        var miCarater7 = (char)97;
        System.out.println("miCarater7 = " + miCarater7);
        
        int letra = 'a';
        System.out.println("letra = " + letra);
        
    }

}
